async def setup(client):
    @client.on(events.NewMessage(pattern=r'\.троль'))
    async def handler(event):
        # Проверяем, что команду отправил именно владелец аккаунта
        if event.sender_id != (await client.get_me()).id:
            return
        
        # Удаляем сообщение с командой
        try:
            await event.delete()
        except:
            pass
        
        # Выбираем случайную фразу
        random_phrase = random.choice(TROLL_PHRASES)
        
        # Если это ответ на чье-то сообщение, упоминаем автора
        if event.is_reply:
            reply_msg = await event.get_reply_message()
            target = await reply_msg.get_sender()
            target_username = f"@{target.username}" if target.username else target.first_name
            await event.respond(f"{target_username}, {random_phrase}")
        else:
            await event.respond(random_phrase)
        
